<template>
  <div class="toast"  v-show="isShow">
    <div>{{message}}</div>
  </div>
</template>

<script>
export default {
  name:'Toast',
  props:{

  },
  data(){
    return {
      message:'',
      isShow:false
    }
  },
  methods:{
    show(message,duration=1500){
      this.isShow = true
      this.message = message

      setTimeout(()=>{
        this.isShow = false
        this.message = ''
      },duration)
    }
  }
}
</script>

<style scoped>
.toast{
  position:fixed;
  z-index: 999;
  padding: 8px 10px;
  top: 50%;
  left: 50%;
  transform: translate(-50%,-50%);
  color: #fff;
  background-color: rgba(0, 0, 0, .8);
  border-radius: 5px;
  font-size: 13px;
}
</style>